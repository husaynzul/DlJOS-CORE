// Map platform IDs/names to icon keys
function resolveKey(platform: string): string | null {
  const p = platform.toLowerCase().replace(/\s+/g, "").replace(/[()]/g, "");
  if (p.includes("instagram")) return "instagram";
  if (p.includes("tiktok") && !p.includes("ads")) return "tiktok";
  if (p.includes("tiktokads") || (p.includes("tiktok") && p.includes("ads"))) return "tiktok";
  if (p.includes("youtube")) return "youtube";
  if (p.includes("facebook")) return "facebook";
  if (p.includes("whatsapp")) return "whatsapp";
  if (p === "x" || p.includes("twitter") || p.includes("xtwitter")) return "x";
  if (p.includes("gmail")) return "gmail";
  if (p.includes("googledrive") || p.includes("drive")) return "googledrive";
  if (p.includes("googleads") || (p.includes("google") && p.includes("ads"))) return "googleads";
  if (p.includes("metaads") || (p.includes("meta") && p.includes("ads"))) return "meta";
  if (p.includes("meta")) return "meta";
  if (p.includes("google")) return "google";
  return null;
}

interface BrandIconProps {
  platform: string;
  size?: number;
  className?: string;
  /** Force white fill for dark backgrounds */
  forceWhite?: boolean;
  /** Show on a colored circular background */
  withBackground?: boolean;
}

export function BrandIcon({ platform, size = 20, className, forceWhite = false, withBackground = false }: BrandIconProps) {
  const key = resolveKey(platform);
  const icon = key ? ICONS[key] : null;

  if (!icon) {
    // Neutral fallback: first letter
    return (
      <div
        className={cn("rounded-lg bg-muted flex items-center justify-center flex-shrink-0 text-muted-foreground font-bold", className)}
        style={{ width: size + 8, height: size + 8, fontSize: size * 0.55 }}
      >
        {platform.charAt(0).toUpperCase()}
      </div>
    );
  }

  // Instagram uses the official gradient
  const isInstagram = key === "instagram";
  const gradientId = `ig-grad-${Math.random().toString(36).slice(2, 7)}`;

  const svgFill = forceWhite ? "white" : (isInstagram ? `url(#${gradientId})` : `#${icon.hex}`);

  // Dark-bg icons: TikTok & X get inverted on dark mode
  const isDarkIcon = icon.hex === "000000";

  const svgEl = (
    <svg
      viewBox="0 0 24 24"
      width={size}
      height={size}
      className={cn(isDarkIcon && !forceWhite && "dark:invert", className)}
      aria-label={platform}
      role="img"
    >
      {isInstagram && !forceWhite && (
        <defs>
          <linearGradient id={gradientId} x1="0%" y1="100%" x2="100%" y2="0%">
            <stop offset="0%" stopColor="#f09433" />
            <stop offset="25%" stopColor="#e6683c" />
            <stop offset="50%" stopColor="#dc2743" />
            <stop offset="75%" stopColor="#cc2366" />
            <stop offset="100%" stopColor="#bc1888" />
          </linearGradient>
        </defs>
      )}
      <path d={icon.path} fill={svgFill} />
    </svg>
  );

  if (withBackground) {
    const bg = isInstagram
      ? "bg-gradient-to-br from-yellow-400 via-pink-500 to-purple-600"
      : isDarkIcon
      ? "bg-zinc-900 dark:bg-zinc-700"
      : "";
    const bgStyle = !isInstagram && !isDarkIcon ? { backgroundColor: `#${icon.hex}20` } : {};
    return (
      <div
        className={cn("rounded-xl flex items-center justify-center flex-shrink-0", bg)}
        style={{ width: size + 14, height: size + 14, ...bgStyle }}
      >
        {isInstagram ? (
          <svg viewBox="0 0 24 24" width={size} height={size} aria-label={platform} role="img">
            <path d={icon.path} fill="white" />
          </svg>
        ) : isDarkIcon ? (
          <svg viewBox="0 0 24 24" width={size} height={size} aria-label={platform} role="img">
            <path d={icon.path} fill="white" />
          </svg>
        ) : (
          svgEl
        )}
      </div>
    );
  }

  return svgEl;
}
